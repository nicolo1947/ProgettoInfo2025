    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String codice = txtCodice.getText();
        String nome = txtNome.getText();
        int durata = 0;

        try {
            durata = Integer.parseInt(txtDurata.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Inserisci un numero valido per la durata.");
            return;
        }

        gestore.aggiungCorso(new Corso(codice, nome, durata));
        System.out.println(S.elencoCorsi);
        
        S.salva();
        //elencoDiscipline.add(nuovo);

        JOptionPane.showMessageDialog(this, "Corso aggiunto con successo!");
        if (callback != null) {
            callback.run();
        }
        dispose(); // chiude il form
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

