    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String CodiceDis = txtCodDis.getText();
        String Nome = txtNome.getText();
        int CFU = 0;    
        
        try {
        CFU = Integer.parseInt(txtCfu.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Inserisci un numero valido per i CFU.");
        return;
    }
        
        String CodiceCorso = txtCodCorso.getText();

    gestore.aggiungiDisciplina(new Disciplina(CodiceDis, Nome, CFU, CodiceCorso));
    S.salva();
    
    JOptionPane.showMessageDialog(this, "Disciplina aggiunto con successo!");
    if (callback != null) callback.run();
    dispose();        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

